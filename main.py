# БЛОК ИНИЦИАЛИЗАЦИИ WEB-СЕРВЕРА
from flask import Flask, render_template
# Создание экземпляра Flask-приложения
app = Flask(__name__)
# БЛОК МАРШРУТИЗАЦИИ (ROUTING)
# КЛЮЧЕВАЯ ФУНКЦИЯ: декоратор, связывающий URL с функцией
@app.route('/')
def hello_world():
    # КЛЮЧЕВАЯ ФУНКЦИЯ: рендеринг HTML-шаблона
    # При обращении к корневому URL возвращается страница index.html
    return render_template("index.html")

# БЛОК ЗАПУСКА СЕРВЕРА
# Запуск приложения на всех сетевых интерфейсах ("0.0.0.0")
# Это позволяет принимать соединения с любого хоста
app.run("0.0.0.0")